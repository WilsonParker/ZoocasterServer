package com.graction.lumi.model.data.fineDust;

import java.util.ArrayList;
import java.util.Collection;

import javax.xml.bind.annotation.XmlRootElement;

import com.graction.lumi.util.StringUtil;


@XmlRootElement
public class FineDustData {
	private Collection<FineDust> fineDust = new ArrayList<FineDust>();
	
	public Collection<FineDust> getFineDust() {
		return fineDust;
	}
	public void setFineDust(Collection<FineDust> fineDust) {
		this.fineDust = fineDust;
	}
	@Override
	public String toString() {
		return "FineDustData [fineDust=" + StringUtil.createString("FineDustData", "FineDust", fineDust) + "]";
	}
}
