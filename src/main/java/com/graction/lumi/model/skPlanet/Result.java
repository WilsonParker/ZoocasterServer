package com.graction.lumi.model.skPlanet;

public class Result {
	private int code;
	private String requestUrl, meesage;

	public int getCode() {
		return code;
	}

	public void setCode(int code) {
		this.code = code;
	}

	public String getRequestUrl() {
		return requestUrl;
	}

	public void setRequestUrl(String requestUrl) {
		this.requestUrl = requestUrl;
	}

	public String getMeesage() {
		return meesage;
	}

	public void setMeesage(String meesage) {
		this.meesage = meesage;
	}

	@Override
	public String toString() {
		return "Result [code=" + code + ", requestUrl=" + requestUrl + ", meesage=" + meesage + "]";
	}

}
